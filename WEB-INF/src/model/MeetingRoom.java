package model;

public class MeetingRoom {

	private int rid;
	private String room;
	private float space;
	private int capacity;
	private int extensionNum;
	private int projector;
	private int screen;
	private int whiteboard;
	private int inputTerminal;

	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public String getRoom() {
		return room;
	}
	public void setRoom(String room) {
		this.room = room;
	}
	public float getSpace() {
		return space;
	}
	public void setSpace(float space) {
		this.space = space;
	}
	public int getCapacity() {
		return capacity;
	}
	public void setCapacity(int capacity) {
		this.capacity = capacity;
	}
	public int getExtensionNum() {
		return extensionNum;
	}
	public void setExtensionNum(int extensionNum) {
		this.extensionNum = extensionNum;
	}
	public int getProjector() {
		return projector;
	}
	public void setProjector(int projector) {
		this.projector = projector;
	}
	public int getScreen() {
		return screen;
	}
	public void setScreen(int screen) {
		this.screen = screen;
	}
	public int getWhiteboard() {
		return whiteboard;
	}
	public void setWhiteboard(int whiteboard) {
		this.whiteboard = whiteboard;
	}
	public int getInputTerminal() {
		return inputTerminal;
	}
	public void setInputTerminal(int inputTerminal) {
		this.inputTerminal = inputTerminal;
	}


}
